    contract_number TEXT,
    rfo_description TEXT,
    rfo_number TEXT,
    customer_type TEXT,
    customer_contact TEXT,
    customer_address TEXT,
    customer_city TEXT,
    customer_state TEXT,
    customer_zip TEXT,
    vendor_contact TEXT,
    vendor_hub_type TEXT,
    vendor_address TEXT,
    vendor_city TEXT,
    vendor_state TEXT,
    vendor_zip TEXT,
    reseller_name TEXT,
    reseller_hub_type TEXT,
    reseller_address TEXT,
    reseller_city TEXT,
    reseller_state TEXT,
    reseller_zip TEXT,
    reseller_phone TEXT,
    report_received_month TEXT,
    purchase_month TEXT,
    brand_name TEXT,
    order_quantity NUMERIC(16, 2),
    unit_price NUMERIC(16, 2),
    invoice_number TEXT,
    po_number TEXT,
    order_date TEXT,
    shipped_date TEXT,
    dir_contract_mgr TEXT,
    contract_type TEXT,
    contract_subtype TEXT,
    contract_start_date TEXT,
    contract_end_date TEXT,
