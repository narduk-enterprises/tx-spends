CREATE TABLE IF NOT EXISTS dir_sales_facts (
    id UUID PRIMARY KEY DEFAULT gen_random_uuid(),
    source_row_hash TEXT NOT NULL UNIQUE,
    fiscal_year INTEGER NOT NULL,
    customer_name_raw TEXT NOT NULL,
    agency_id UUID REFERENCES agencies(id),
    vendor_name_raw TEXT NOT NULL,
    purchase_amount NUMERIC(16, 2) NOT NULL,
    contract_number TEXT,
    rfo_description TEXT,
    order_quantity NUMERIC(16, 2),
    unit_price NUMERIC(16, 2),
    invoice_number TEXT,
    po_number TEXT,
    shipped_date DATE,
    contract_type TEXT,
    contract_subtype TEXT,
    staffing_contractor_name TEXT,
    staffing_title TEXT,
    staffing_start_date DATE,
    source_loaded_at TIMESTAMPTZ NOT NULL,
    created_at TIMESTAMPTZ NOT NULL DEFAULT NOW(),
    updated_at TIMESTAMPTZ NOT NULL DEFAULT NOW()
);

CREATE INDEX IF NOT EXISTS idx_dsf_agency_fy ON dir_sales_facts (agency_id, fiscal_year);
CREATE INDEX IF NOT EXISTS idx_dsf_po_number ON dir_sales_facts (po_number);
